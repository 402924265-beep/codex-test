export function projectImpactSummary(items = PROJECT_SEEDS) {
  const bucket = (impactType) => {
    const rows = items.filter((item) => item.impactType === impactType);
    return {
      planned: sum(rows.map((item) => item.plannedImpact)),
      actual: sum(rows.map((item) => item.actualCumulative))
    };
  };
  return {
    inflation: bucket("inflation"),
    wage: bucket("wage"),
    scale: bucket("scale")
  };
}

function project(id, lead, category, strategy, projectName, owner, timing, plannedImpact, actualCumulative, progress, impactType) {
  return {
    id,
    type: category === "上涨因素" ? "increase" : "decrease",
    lead,
    category,
    strategy,
    project: projectName,
    owner,
    timing,
    plannedImpact,
    actualCumulative,
    progress,
    impactType,
    budgetMonths: Array(12).fill(0),
    actualMonths: Array(12).fill(0)
  };
}

function sum(values) {
  return values.reduce((total, value) => total + (Number.isFinite(value) ? value : 0), 0);
}
